package com.mycompany.infotrabajogrupo12.entities;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author Juand
 */
@Entity
@Table(name = "solicitar")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Solicitar.findAll", query = "SELECT s FROM Solicitar s"),
    @NamedQuery(name = "Solicitar.findById", query = "SELECT s FROM Solicitar s WHERE s.id = :id"),
    @NamedQuery(name = "Solicitar.findByOferta", query = "SELECT s FROM Solicitar s WHERE s.oferta = :oferta"),
    @NamedQuery(name = "Solicitar.findByEmail", query = "SELECT s FROM Solicitar s WHERE s.email = :email")})
public class Solicitar implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Basic(optional = false)
    @NotNull
    @Column(name = "oferta")
    private int oferta;
    // @Pattern(regexp="[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?", message="Invalid email")//if the field contains email address consider using this annotation to enforce field validation
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 255)
    @Column(name = "email")
    private String email;

    public Solicitar() {
    }

    public Solicitar(Integer id) {
        this.id = id;
    }

    public Solicitar(Integer id, int oferta, String email) {
        this.id = id;
        this.oferta = oferta;
        this.email = email;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public int getOferta() {
        return oferta;
    }

    public void setOferta(int oferta) {
        this.oferta = oferta;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Solicitar)) {
            return false;
        }
        Solicitar other = (Solicitar) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.mycompany.infotrabajogrupo12.entities.Solicitar[ id=" + id + " ]";
    }
    
}
